function h(text) {
  return `<div dir="rtl">${text}</div>`;
}

function a(text, correct, feedback) {
  return {
    correct,
    tipsAndFeedback: {
      chosenFeedback: h(feedback),
    },
    text: h(text),
  };
}

function q(question, answers) {
  return { question, answers };
}

